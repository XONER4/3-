async def check_rank_upgrade(user: User, session: AsyncSession):
    if user.rank_manual:
        return None
    total = user.total_earned
    new_rank = "Рядовой"
    if total >= 1600000:
        new_rank = "Старший лейтенант"
    elif total >= 800000:
        new_rank = "Лейтенант"
    elif total >= 400000:
        new_rank = "Старший сержант"
    elif total >= 200000:
        new_rank = "Сержант"
    elif total >= 100000:
        new_rank = "Младший сержант"
    elif total >= 50000:
        new_rank = "Ефрейтор"
    
    if new_rank != user.rank:
        old_rank = user.rank
        user.rank = new_rank
        await session.commit()
        return (old_rank, new_rank)
    return None

async def add_transaction(session: AsyncSession, user_id: int, amount: float, type_: str, description: str = None):
    """Добавляет запись о транзакции в базу данных."""
    transaction = Transaction(
        user_id=user_id,
        amount=amount,
        type=type_,
        description=description
    )
    session.add(transaction)
    await session.commit()

async def add_medal(user: User, medal_name: str, session: AsyncSession, give_bonus: bool = True):
    medals = json.loads(user.medals) if user.medals else []
    if medal_name not in medals:
        medals.append(medal_name)
        user.medals = json.dumps(medals)
        if give_bonus:
            bonus = MEDAL_BONUSES.get(medal_name, 5000)  # по умолчанию 5000
            user.balance += bonus
            user.total_earned += bonus
            await add_transaction(session, user.id, bonus, "medal_bonus", f"Бонус за медаль '{medal_name}'")
        await session.commit()
        return True
    return False

def calculate_deposit_payout(amount: float, hours_passed: float) -> float:
    return amount * (1 + 0.2 * hours_passed)

def calculate_credit_debt(original: float, hours_passed: float) -> float:
    periods = hours_passed / 5.0
    return original * (1 + 0.3 * periods)

